/*
 *  Autocomplete  for Google Places
 */

(function ($) {
    function log(message) {
        $("<div>").text(message).prependTo("#log");
        $("#log").scrollTop(0);
    }

    $("#birds").autocomplete({
        source: function (request, response) {
            $.ajax({
                url: "search.php",
                dataType: "jsonp",
                data: {
                    term: request.term
                },
                success: function (data) {
                    response(data);
                }
            });
        },
        minLength: 2,
        select: function (event, ui) {
            log("Selected: " + ui.item.value + " aka " + ui.item.id);
        }
    });

})(jQuery);